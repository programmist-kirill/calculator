class main:
    def plus(a, b):
        result = int(a) + int(b)
        print(f'результат: {result}')

    def minus(a, b):
        result = int(a) - int(b)
        print(f'результат: {result}')

    def multiply(a, b):
        result = int(a) * int(b)
        print(f'результат: {result}')

    def divide(a, b):
        result = int(a) // int(b)
        print(f'результат: {result}')